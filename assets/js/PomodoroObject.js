class Pomodoro {
    constructor(
        time=25, 
        shortBreak=5, 
        longBreak=15, 
        longBreakAfter=4, 
        stopBell=1, 
        startBell=1
        ) 
    {
        this.time = time;
        this.shortBreak = shortBreak;
        this.longBreak = longBreak;
        this.longBreakAfter = longBreakAfter;
        this.stopBell = stopBell;
        this.startBell = startBell;
    }
}



